export const translations = { he, en } as const;
export type TranslationKey = keyof typeof he;

export const dirByLang: Record<Lang, Dir> = {
  he: 'rtl',
  en: 'ltr',
};

export function t(lang: Lang, key: TranslationKey): string {
  return translations[lang][key] ?? key;
}

/** Replace `{param}` placeholders in a global UI translation string. */
export function formatTranslation(
  lang: Lang,
  key: TranslationKey,
  params: Record<string, string | number>,
): string {
  return Object.entries(params).reduce(
    (text, [param, value]) => text.replace(new RegExp(`\\{${param}\\}`, 'g'), String(value)),
    t(lang, key),
  );
}

